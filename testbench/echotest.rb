require 'pp'
require 'rubygems'
require 'serialport'
require 'png'

# Requires the gems serialport and png
# 
# $ gem install ruby-serialport
# $ gem install png


@sp = SerialPort.new '/dev/tty.usbserial-A4001o6L', 19200

@canvas = PNG::Canvas.new(500,500)
@x = 0
@y = 0
@ready_for_command = true
@last_byte = Time.now

@input = ''

@step_count = 0

Thread.new do
  step_mode = false
  @sp.each_byte do |byte| 
    puts byte.chr
       
    @last_byte = Time.now
    @input << byte.chr
    if byte == 126 # ('~')
      step_mode = true          
      @input << '[step mode on]'
    else
      if step_mode 
        @step_count += 1
        if byte == 10
          @input << '[step mode off]'
          step_mode = false
          @ready_for_command = true
        else
          step = byte-33
          puts @step_count
          if (step & 1) != 0
            if (step & 8) != 0
              puts "x-1"
              @x+=1
            else
              puts "x+1"
              @x-=1
            end
          end
          if (step & 2) != 0
            if (step & 16) != 0
              puts "y-1"
              @y+=1
            else
              puts "y+1"
              @y-=1
            end
          end
        end
      end
    end
    @canvas[250-@x,250-@y] = PNG::Color::Black    
  end
end

def send_command(command)
  sleep(0.5)
  puts "Sent: #{command}"
  @input << "[sent #{command}]"
  @sp.write("#{command}\r")
  @ready_for_command = false
end


